#include <iostream>
#include "sceneAccelPitchStretch.h"

float sceneAccelPitchStretch::getNextSample() {
    float x = pitchStretch->play(pitchMA.value(), rateVal, 0.05, 2, 0.0);    
    if (motionTriggering) {
        x *= ampMA.value();
    }
    return x;
}
